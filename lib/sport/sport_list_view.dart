                    borderRadius: const BorderRadius.all(Radius.circular(6.0)),
                    child: Stack(
                      children: <Widget>[
                        Column(
                          children: <Widget>[
                            AspectRatio(
                              aspectRatio: 3,
                              child: Image.asset(
                                sportData.imagePath,
                                fit: BoxFit.cover,
                              ),
                            ),
                            Container(
                              color: SportAppTheme.buildLightTheme()
                                  .backgroundColor,
